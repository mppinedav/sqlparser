package model.parser;

public class UnAggregateExpVO {
	private String unAggregateExp;
	private String singleExp;
	private String existsFlag;
	
	public String getExistsFlag() {
		return existsFlag;
	}
	public void setExistsFlag(String existsFlag) {
		this.existsFlag = existsFlag;
	}
	public String getSingleExp() {
		return singleExp;
	}
	public void setSingleExp(String singleExp) {
		this.singleExp = singleExp;
	}
	public String getUnAggregateExp() {
		return unAggregateExp;
	}
	public void setUnAggregateExp(String unAggregateExp) {
		this.unAggregateExp = unAggregateExp;
	}
	
	
}
